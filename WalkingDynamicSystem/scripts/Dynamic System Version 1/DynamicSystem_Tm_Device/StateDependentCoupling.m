function [ Q ] = StateDependentCoupling( Sg,u )

% LoadParameter;
w(1) = 0.1; w(2) = 0.2;

Q(1) = Sg(2)*w(1)*fun(u(4))+Sg(5)*w(1)*fun(u(6));
Q(2) = Sg(3)*w(1)*fun(u(3))+Sg(6)*w(1)*fun(u(5));
Q(3) = (Sg(4)+Sg(5)-Sg(6))*w(1)*fun(u(7)) ...
    +(-Sg(1)-Sg(2)+Sg(3))*w(1)*fun(u(8)) ...
    +(-Sg(1)-Sg(2)+Sg(5))*w(1)*fun(u(11)) ...
    +(Sg(3)+Sg(4)-Sg(6))*w(1)*fun(u(12));
Q(4) = -Q(3);
Q(5) = (Sg(1)+Sg(2)-Sg(3))*w(1)*fun(u(9)) ...
    +(-Sg(4)-Sg(5)+Sg(6))*w(1)*fun(u(10)) ...
    +(-Sg(4)-Sg(5)+Sg(2))*w(1)*fun(u(13)) ...
    +(Sg(6)+Sg(1)-Sg(3))*w(1)*fun(u(14));
Q(6) = -Q(5);
Q(7) = (-Sg(3)+Sg(4)+Sg(5))*w(2)*fun(u(3)) ...
    +(-Sg(1)-Sg(2)+Sg(6))*w(2)*fun(u(4)) ...
    +(-Sg(1)-Sg(2)+Sg(5))*w(1)*fun(u(11)) ...
    +(-Sg(3)+Sg(4)+Sg(6))*w(1)*fun(u(12));
Q(8) = -Q(7);
Q(9) = (-Sg(6)+Sg(1)+Sg(2))*w(2)*fun(u(5)) ...
    +(-Sg(4)-Sg(5)+Sg(3))*w(2)*fun(u(6)) ...
    +(-Sg(4)-Sg(5)+Sg(2))*w(1)*fun(u(13)) ...
    +(-Sg(6)+Sg(1)+Sg(3))*w(1)*fun(u(14));
Q(10) = -Q(9);
Q(11) = (-Sg(3)-Sg(4)+Sg(5))*w(2)*fun(u(3)) ...
    +(Sg(1)+Sg(2)-Sg(6))*w(2)*fun(u(4)) ...
    +(-Sg(4)+Sg(5)-Sg(6))*w(1)*fun(u(7)) ...
    +(Sg(1)+Sg(2)-Sg(3))*w(1)*fun(u(8));
Q(12) = -Q(11);
Q(13) = (-Sg(6)-Sg(1)+Sg(2))*w(2)*fun(u(5)) ...
    +(Sg(4)+Sg(5)-Sg(3))*w(2)*fun(u(6)) ...
    +(-Sg(1)+Sg(2)-Sg(3))*w(1)*fun(u(9)) ...
    +(Sg(4)+Sg(5)-Sg(6))*w(1)*fun(u(10));
Q(14) = -Q(13);

end

