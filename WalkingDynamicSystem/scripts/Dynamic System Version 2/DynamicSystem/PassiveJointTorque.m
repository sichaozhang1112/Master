function [ Tp ] = PassiveJointTorque( theta,theta_dot )

% LoadParameter;
k(1) = 1000.0; k(2) = 500.0;
b(1) = 10.0; b(2) = 1.0; b(3) = 100.0; b(4) = 1000.0;

Tp(1) = b(1)*(theta_dot(1)-theta_dot(2)) ...
    + b(3)*fun(theta(1)-theta(2)-7*pi/18)*(theta_dot(1)-theta_dot(2)) + k(1)*fun(theta(1)-theta(2)-7*pi/18) ...
    + b(3)*fun(theta(2)-theta(1)-pi/9)*(theta_dot(1)-theta_dot(2)) - k(1)*fun(theta(2)-theta(1)-pi/9);
%%
Tp(2) = b(2)*(theta_dot(2)-theta_dot(3)) ...
    + b(3)*fun(theta(2)-theta(3)-pi/4)*(theta_dot(2)-theta_dot(3)) + k(2)*fun(theta(2)-theta(3)-pi/4) ...
    + b(3)*fun(theta(3)-theta(2)-pi/12)*(theta_dot(2)-theta_dot(3)) - k(2)*fun(theta(3)-theta(2)-pi/12);
Tp(3) = b(2)*(theta_dot(2)-theta_dot(4)) ...
    + b(3)*fun(theta(2)-theta(4)-pi/4)*(theta_dot(2)-theta_dot(4)) + k(2)*fun(theta(2)-theta(4)-pi/4) ...
    + b(3)*fun(theta(4)-theta(2)-pi/12)*(theta_dot(2)-theta_dot(4)) - k(2)*fun(theta(4)-theta(2)-pi/12);

Tp(4) = - b(2)*(theta_dot(3)-theta_dot(5)) ...
    - b(4)*fun(theta(3)-theta(5)+pi/36)*(theta_dot(3)-theta_dot(5)) - k(1)*fun(theta(3)-theta(5)+pi/36) ...
    - b(3)*fun(theta(5)-theta(3)-7*pi/18)*(theta_dot(3)-theta_dot(5)) + k(1)*fun(theta(5)-theta(3)-7*pi/18);
Tp(5) = - b(2)*(theta_dot(4)-theta_dot(6)) ...
    - b(4)*fun(theta(4)-theta(6)+pi/36)*(theta_dot(4)-theta_dot(6)) - k(1)*fun(theta(4)-theta(6)+pi/36) ...
    - b(3)*fun(theta(6)-theta(4)-7*pi/18)*(theta_dot(4)-theta_dot(6)) + k(1)*fun(theta(6)-theta(4)-7*pi/18);

Tp(6) = b(2)*(theta_dot(5)-theta_dot(7)) ...
    + b(3)*fun(theta(5)-theta(7)-0.5760-2*pi/9)*(theta_dot(5)-theta_dot(7)) ...
    + k(1)*fun(theta(5)-theta(7)-0.5760-2*pi/9) ...
    + b(3)*fun(0.5760-theta(5)+theta(7)-pi/6)*(theta_dot(5)-theta_dot(7)) ...
    - k(1)*fun(0.5760-theta(5)+theta(7)-pi/6);
Tp(7) = b(2)*(theta_dot(6)-theta_dot(8)) ...
    + b(3)*fun(theta(6)-theta(8)-0.5760-2*pi/9)*(theta_dot(6)-theta_dot(8)) ...
    + k(1)*fun(theta(6)-theta(8)-0.5760-2*pi/9) ...
    + b(3)*fun(0.5760-theta(6)+theta(8)-pi/6)*(theta_dot(6)-theta_dot(8)) ...
    - k(1)*fun(0.5760-theta(6)+theta(8)-pi/6);

%%
end

